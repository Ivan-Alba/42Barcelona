int	ft_is_prime(int nb)
{
	int	i;
	int	count;

	if (nb <= 1)
		return (0);
	count = 0;
	i = nb - 1;
	while (i > 1)
	{
		if (nb % i == 0)
			count++;
		i--;
	}
	if (count == 0)
		return (1);
	return (0);
}
