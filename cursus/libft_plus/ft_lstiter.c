#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*curr;

	if (!lst || !f)
		return ;
	curr = lst;
	while (curr -> next)
	{
		f(curr -> content);
		curr = curr -> next;
	}
	f(curr -> content);
}
